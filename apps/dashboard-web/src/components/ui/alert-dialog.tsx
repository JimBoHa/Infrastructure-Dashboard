        "inline-flex items-center justify-center rounded-lg bg-destructive px-4 py-2.5 text-sm font-semibold text-white shadow-xs hover:bg-destructive/90 focus:outline-hidden focus:ring-2 focus:ring-destructive/30 disabled:pointer-events-none disabled:opacity-50",
